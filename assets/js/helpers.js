
function Helpers() {

  Helpers.prototype.getColor = function(number) {
    switch (number) {
      case 1:
        return "#fcfca8";
        break;
      case 2:
        return "#f8aff2";
        break;
      case 3:
        return "#8c8af8";
        break;
      case 4:
        return "#fd997a";
      case 5:
        return "#b0fabb";
        break;
      case 6:
        return "#ffd95d";
        break;
      case 7:
        return "#afc2f8";
    }
  }

}